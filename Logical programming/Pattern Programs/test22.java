public class test22 {

    public static void main(String []args)
    {
        int n=5;
        for(int i=1;i>=1;i++)
        {
            for(int j=1;j<=n-1;j++)
            {
                System.out.print("  ");
            }

            for(int k=1;k<=1;k++)
            {
                System.out.print("* ");
            }

            System.out.println();
        }
    }
    
}

